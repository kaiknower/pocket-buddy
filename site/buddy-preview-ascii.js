const hats = {
  none: "",
  crown: "   \\^^^/    ",
  tophat: "   [___]    ",
  propeller: "    -+-     ",
  halo: "   (   )    ",
  wizard: "    /^\\\\     ",
  beanie: "   (___)    ",
  tinyduck: "    ,>      ",
}

export function renderBuddyAsciiPreview(state, frame = 0) {
  const frames = spriteFrames[state.species] || spriteFrames.dragon
  const current = frames[frame % frames.length].map((line) => line.replaceAll('{E}', state.eye))
  if (state.hat !== 'none' && hats[state.hat] && !current[0].trim()) {
    current[0] = hats[state.hat]
  }
  if (!current[0].trim() && frames.every((variant) => !variant[0].trim())) {
    current.shift()
  }
  return current.join('\n')
}
